function tokenize(blob) {
  return blob
    .toUpperCase()
    .replace(/[^A-Z\s]/g, ' ')
    .split(/\s+/)
    .map((w) => w.trim())
    .filter((w) => w.length >= 3 && w.length <= 14);
}

function expandBanks() {
  const file = path.join(root, 'src/lib/wordBanks.ts');
  let src = fs.readFileSync(file, 'utf8');

  const map = {
    mythology: EXTRA.mythology,
    history: EXTRA.history,
    travel: EXTRA.travel,
    technology: EXTRA.technology,
    movies: EXTRA.movies + ' ' + EXTRA.movies_more,
    holiday: EXTRA.holiday,
    kids: EXTRA.kids,
    animals: EXTRA.animals_more,
  };

  for (const [cat, blob] of Object.entries(map)) {
    const re = new RegExp(`(${cat}:\\s*\\[)([\\s\\S]*?)(\\n  \\],)`);
    const m = src.match(re);
    if (!m) {
      console.warn('skip', cat);
      continue;
    }
    const existing = [...m[2].matchAll(/'([A-Z][A-Z0-9]*)'/g)].map((x) => x[1]);
    const added = tokenize(blob);
    const merged = [...new Set([...existing, ...added])].filter(
      (w) => w.length >= 3 && w.length <= 14,
    );
    const lines = [];
    for (let i = 0; i < merged.length; i += 6) {
      const chunk = merged.slice(i, i + 6).map((w) => `'${w}'`);
      lines.push(`    ${chunk.join(', ')},`);
    }
    src = src.replace(re, `$1\n${lines.join('\n')}$3`);
    console.log(cat, existing.length, '->', merged.length);
  }

  fs.writeFileSync(file, src);
}

