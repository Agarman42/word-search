function expandPacks() {
  const file = path.join(root, 'src/lib/packLevels.ts');
  let src = fs.readFileSync(file, 'utf8');

  const blocks = [];
  for (const [id, blob] of Object.entries(PACK_POOLS)) {
    const words = [...new Set(tokenize(blob))].filter((w) => w.length >= 3 && w.length <= 14);
    console.log('pack', id, words.length);
    const lines = [];
    for (let i = 0; i < words.length; i += 6) {
      const chunk = words.slice(i, i + 6).map((w) => `'${w}'`);
      lines.push(`    ${chunk.join(', ')},`);
    }
    blocks.push(`  '${id}': [\n${lines.join('\n')}\n  ],`);
  }

  const newPools = `export const PACK_WORD_POOLS: Partial<Record<string, string[]>> = {\n${blocks.join('\n')}\n};\n`;
  src = src.replace(
    /export const PACK_WORD_POOLS: Partial<Record<string, string\[\]>> = \{[\s\S]*?\n\};\n/,
    newPools,
  );
  fs.writeFileSync(file, src);
}

